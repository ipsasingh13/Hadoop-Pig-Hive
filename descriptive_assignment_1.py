import matplotlib.pyplot as plt
import seaborn as sns

# Scatter plot with regression line
plt.figure(figsize=(8,6))
sns.regplot(x='StudyHours', y='Marks', data=df, scatter_kws={'s':70, 'alpha':0.7}, line_kws={'color':'red'})

plt.title('Trend between StudyHours and Marks')
plt.xlabel('Study Hours')
plt.ylabel('Marks')
plt.show()

# Calculate correlation coefficient
corr = df['StudyHours'].corr(df['Marks'])
print(f"Correlation between StudyHours and Marks: {corr:.2f}")

# Calculate average marks by gender
avg_marks_by_gender = df.groupby('Gender')['Marks'].mean()

print("Average Marks by Gender:")
print(avg_marks_by_gender)

# Identify which gender has higher marks
higher_gender = avg_marks_by_gender.idxmax()
print(f"\nOn average, {higher_gender} students score higher marks.")

