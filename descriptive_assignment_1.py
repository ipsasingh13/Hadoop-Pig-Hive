"""Part B: Data **Cleaning**"""

# Handle missing values

# Replace missing StudyHours with the mean
df['StudyHours'].fillna(df['StudyHours'].mean(), inplace=True)

# Replace missing Marks with the median
df['Marks'].fillna(df['Marks'].median(), inplace=True)

# Replace missing Attendance with the column mean
df['Attendance'].fillna(df['Attendance'].mean(), inplace=True)

# Verify that missing values are handled
print("\nMissing Value Counts after imputation:")
print(df.isnull().sum())

# Remove duplicate rows
df.drop_duplicates(inplace=True)

# Verify the shape after removing duplicates
print("\nShape after removing duplicates:", df.shape)

# Optionally, check again for duplicates
print("Remaining duplicate rows:", df.duplicated().sum())

# 3. Save the cleaned dataset as student_cleaned.csv
df.to_csv('student_cleaned.csv', index=False)

